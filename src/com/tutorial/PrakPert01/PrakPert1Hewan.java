    String jumlah;
    String jenis;
    String hidup;

    public void setName(String nama){
        this.nama = nama;
    }
    public void setMany(String jumlah){
        this.jumlah = jumlah;
    }
    public void setKind(String jenis){
        this.jenis = jenis;
    }
    public void setLife(String hidup){
        this.hidup = hidup;
    }

    public String getNama(){
        return nama;
    }
    public String getJmlh(){
        return nama;
    }
    public String getJenis(){
        return nama;
    }
    public String getHidup(){
        return nama;
    }
